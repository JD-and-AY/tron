		{
			UU = 0; 
			DD = 0;
			RR = 1;
			LL = 0;
		}
		if((GPIO_PORTC_RIS_R & 0x40)== 0x40)									//if player 2 has pressed down
		{
			UU = 0; 
			DD = 1;
			RR = 0;
			LL = 0;
		}
