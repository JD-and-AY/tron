	void GPIOPortB_Handler(void) {	
		if((GPIO_PORTB_RIS_R & 0x01)== 0x01)            			 //if player 1 has pressed up
		{
			U = 1; 
			D = 0;
			R = 0;
			L = 0;   
		}
		if((GPIO_PORTB_RIS_R & 0x02)== 0x02)         			     //if player 1 has pressed Right
		{
			U = 0; 
			D = 0;
			R = 1;
			L = 0;   
		}
		if((GPIO_PORTB_RIS_R & 0x04)== 0x04)            			 //if player 1 has pressed down
		{
			U = 0; 
			D = 1;
			R = 0;
			L = 0;   
		}
		if((GPIO_PORTB_RIS_R & 0x08)== 0x08)           				 //if player 1 has pressed left
		{
			U = 0; 
			D = 0;
			R = 0;
			L = 1;   
		}
		GPIO_PORTB_ICR_R = 0x2F;             				           // Interrupt flag set (Reset for hardware interrupts)
	}
		
